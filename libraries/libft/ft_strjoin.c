#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len;
	int		i;
	int		j;

	if (s1 && s2)
	{
		i = 0;
		j = 0;
		len = ft_strlen((char*)s1) + ft_strlen((char*)s2);
		if (!(str = (char*)malloc(sizeof(char) * len + 1)))
			return (0);
		while (s1[i])
		{
			str[i] = s1[i];
			i++;
		}
		while (s2[j])
			str[i++] = s2[j++];
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
