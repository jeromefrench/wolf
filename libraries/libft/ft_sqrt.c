#include "libft.h"

int	ft_sqrt(int nb)
{
	int n;
	int sq;

	n = 1;
	if (nb == 1)
		return (1);
	while (n <= nb / 2)
	{
		sq = n * n;
		if (sq == nb)
		{
			return (n);
		}
		n++;
	}
	return (0);
}
