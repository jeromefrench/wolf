#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t nb)
{
	int		n;
	size_t	p;

	n = 0;
	p = 0;
	while (s1[n] != '\0')
		n++;
	while (nb > p && s2[p])
	{
		s1[n] = s2[p];
		n++;
		p++;
	}
	s1[n] = '\0';
	return (s1);
}
