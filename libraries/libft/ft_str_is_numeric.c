int	ft_str_is_numeric(char *str)
{
	int n;

	n = 0;
	if (str[n] == '\0')
		return (1);
	while (str[n] != '\0')
	{
		if ((str[n] >= 0 && str[n] <= 47) || str[n] >= 58)
			return (0);
		n++;
	}
	return (1);
}
